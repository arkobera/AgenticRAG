"""
Google Generative AI-based Judge for RAG Pipeline Evaluation.

Uses Google's Gemini model as an LLM judge to evaluate RAG-generated answers
against reference answers and context relevance.
"""

import json
import os
from typing import Dict, List, Any, Optional, Tuple
from dataclasses import dataclass
import time

try:
    import google.generativeai as genai
    GOOGLE_API_AVAILABLE = True
except ImportError:
    GOOGLE_API_AVAILABLE = False

from src.logger import get_logger

logger = get_logger(__name__)


@dataclass
class JudgeScore:
    """Score from the judge evaluation."""
    query: str
    generated_answer: str
    reference_answer: str
    context: str
    
    # Scores (0-10 scale)
    answer_relevance_score: float
    answer_faithfulness_score: float
    answer_completeness_score: float
    context_utilization_score: float
    
    # Overall score (0-10 scale)
    overall_score: float
    
    # Reasoning
    reasoning: str
    strengths: List[str]
    weaknesses: List[str]

    def to_dict(self) -> Dict[str, Any]:
        """Convert to dictionary."""
        return {
            "query": self.query,
            "generated_answer": self.generated_answer[:500],  # Truncate for storage
            "reference_answer": self.reference_answer[:500],
            "context": self.context[:300],
            "answer_relevance_score": self.answer_relevance_score,
            "answer_faithfulness_score": self.answer_faithfulness_score,
            "answer_completeness_score": self.answer_completeness_score,
            "context_utilization_score": self.context_utilization_score,
            "overall_score": self.overall_score,
            "reasoning": self.reasoning,
            "strengths": self.strengths,
            "weaknesses": self.weaknesses,
        }


class GoogleGenerativeAIJudge:
    """
    LLM Judge using Google's Generative AI API (Gemini).
    
    This judge evaluates RAG-generated answers using multiple criteria:
    - Answer Relevance: How well does the answer address the query?
    - Answer Faithfulness: Is the answer grounded in the provided context?
    - Answer Completeness: Does the answer cover all important aspects?
    - Context Utilization: How well is the provided context used?
    """
    
    def __init__(self, model_name: str = "gemini-2.5-flash", api_key: Optional[str] = None):
        """
        Initialize Google Generative AI Judge.
        
        Args:
            model_name: Google Generative AI model name (default: gemini-2.5-flash)
            api_key: Google API key (if None, uses GOOGLE_API_KEY env variable)
            
        Raises:
            ImportError: If google-generativeai is not installed
            ValueError: If API key is not provided or found
        """
        logger.info(f"Initializing GoogleGenerativeAIJudge with model: {model_name}")
        
        if not GOOGLE_API_AVAILABLE:
            logger.error("google-generativeai is not installed")
            raise ImportError(
                "google-generativeai is not installed. "
                "Install it with: pip install google-generativeai"
            )
        
        api_key = api_key or os.getenv("GOOGLE_API_KEY")
        if not api_key:
            logger.error("Google API key not found")
            raise ValueError(
                "Google API key not found. "
                "Set GOOGLE_API_KEY environment variable or pass api_key parameter."
            )
        
        genai.configure(api_key=api_key)
        self.model = genai.GenerativeModel(model_name)
        self.model_name = model_name
        
        logger.debug("GoogleGenerativeAIJudge model configured successfully")
        
        # Safety settings - be more permissive for evaluation
        self.safety_settings = [
            {
                "category": "HARM_CATEGORY_HARASSMENT",
                "threshold": "BLOCK_NONE",
            },
            {
                "category": "HARM_CATEGORY_HATE_SPEECH",
                "threshold": "BLOCK_NONE",
            },
            {
                "category": "HARM_CATEGORY_SEXUALLY_EXPLICIT",
                "threshold": "BLOCK_NONE",
            },
            {
                "category": "HARM_CATEGORY_DANGEROUS_CONTENT",
                "threshold": "BLOCK_NONE",
            },
        ]
        
        logger.info("GoogleGenerativeAIJudge initialized successfully")
    
    def evaluate(
        self,
        query: str,
        generated_answer: str,
        reference_answer: str,
        context: str = "",
    ) -> JudgeScore:
        """
        Evaluate a generated answer using Google Generative AI.
        
        Args:
            query: The original query
            generated_answer: The answer generated by the RAG system
            reference_answer: The reference/ground truth answer
            context: The context used to generate the answer
            
        Returns:
            JudgeScore object with evaluation results
        """
        logger.debug(f"Evaluating answer for query: '{query[:60]}...'")
        
        # Build the evaluation prompt
        prompt = self._build_evaluation_prompt(
            query, generated_answer, reference_answer, context
        )
        
        # Get evaluation from Google Generative AI
        response_text = self._call_google_api(prompt)
        
        # Parse the response to extract scores
        judge_score = self._parse_judge_response(
            response_text, query, generated_answer, reference_answer, context
        )
        
        logger.debug(f"Evaluation complete. Overall score: {judge_score.overall_score:.1f}/10")
        return judge_score
    
    def evaluate_batch(
        self,
        evaluations: List[Dict[str, str]],
        batch_size: int = 5,
        delay_seconds: float = 1.0,
    ) -> List[JudgeScore]:
        """
        Evaluate multiple answers in batch.
        
        Args:
            evaluations: List of dicts with keys: query, generated_answer, 
                        reference_answer, context (optional)
            batch_size: Number of evaluations to process before adding delay
            delay_seconds: Delay in seconds between batches
            
        Returns:
            List of JudgeScore objects
        """
        logger.info(f"Starting batch evaluation of {len(evaluations)} items...")
        results = []
        
        for i, evaluation in enumerate(evaluations):
            try:
                score = self.evaluate(
                    query=evaluation.get("query", ""),
                    generated_answer=evaluation.get("generated_answer", ""),
                    reference_answer=evaluation.get("reference_answer", ""),
                    context=evaluation.get("context", ""),
                )
                results.append(score)
                
                # Progress logging
                if (i + 1) % batch_size == 0:
                    logger.info(f"Evaluated {i + 1}/{len(evaluations)} items")
                
                # Add delay between batches to avoid rate limiting
                if (i + 1) % batch_size == 0 and i + 1 < len(evaluations):
                    logger.debug(f"Rate limit pause: {delay_seconds}s")
                    time.sleep(delay_seconds)
                    
            except Exception as e:
                print(f"  Error evaluating item {i}: {e}")
                # Create a failed score
                results.append(
                    JudgeScore(
                        query=evaluation.get("query", ""),
                        generated_answer=evaluation.get("generated_answer", ""),
                        reference_answer=evaluation.get("reference_answer", ""),
                        context=evaluation.get("context", ""),
                        answer_relevance_score=0.0,
                        answer_faithfulness_score=0.0,
                        answer_completeness_score=0.0,
                        context_utilization_score=0.0,
                        overall_score=0.0,
                        reasoning=f"Error during evaluation: {str(e)}",
                        strengths=[],
                        weaknesses=["Evaluation failed"],
                    )
                )
        
        return results
    
    def _build_evaluation_prompt(
        self,
        query: str,
        generated_answer: str,
        reference_answer: str,
        context: str,
    ) -> str:
        """Build the evaluation prompt for the judge."""
        
        context_section = ""
        if context:
            context_section = f"\n\n**Context provided to RAG system:**\n{context[:1000]}"
        
        prompt = f"""You are an expert evaluator for Retrieval-Augmented Generation (RAG) systems.
Evaluate the generated answer based on multiple criteria and provide scores.

**Query:**
{query}

**Reference Answer (expected/correct answer):**
{reference_answer}

**Generated Answer (from RAG system):**
{generated_answer}
{context_section}

Please evaluate the generated answer on the following criteria (score 0-10):

1. **Answer Relevance** (0-10): How well does the generated answer address the query?
2. **Answer Faithfulness** (0-10): Is the generated answer grounded in the provided context and not making unsupported claims?
3. **Answer Completeness** (0-10): Does the generated answer cover all important aspects of the reference answer?
4. **Context Utilization** (0-10): How effectively does the generated answer use the provided context?

Provide your evaluation in the following JSON format:
{{
    "answer_relevance_score": <0-10>,
    "answer_faithfulness_score": <0-10>,
    "answer_completeness_score": <0-10>,
    "context_utilization_score": <0-10>,
    "overall_score": <0-10>,
    "reasoning": "<brief explanation of the scores>",
    "strengths": ["<strength 1>", "<strength 2>"],
    "weaknesses": ["<weakness 1>", "<weakness 2>"]
}}

Only output the JSON object, no other text."""
        
        return prompt
    
    def _call_google_api(self, prompt: str) -> str:
        """
        Call Google Generative AI API with the prompt.
        
        Args:
            prompt: The evaluation prompt
            
        Returns:
            Response text from the API
        """
        try:
            response = self.model.generate_content(
                prompt,
                safety_settings=self.safety_settings,
                generation_config={
                    "temperature": 0.3,
                    "top_p": 0.8,
                    "top_k": 40,
                    "max_output_tokens": 1024,
                }
            )
            return response.text
        except Exception as e:
            raise RuntimeError(f"Error calling Google Generative AI API: {e}")
    
    def _parse_judge_response(
        self,
        response_text: str,
        query: str,
        generated_answer: str,
        reference_answer: str,
        context: str,
    ) -> JudgeScore:
        """
        Parse the judge response and extract scores.
        
        Args:
            response_text: The response from Google API
            query: The original query
            generated_answer: The generated answer
            reference_answer: The reference answer
            context: The context
            
        Returns:
            JudgeScore object
        """
        try:
            # Extract JSON from response
            json_str = self._extract_json(response_text)
            scores_dict = json.loads(json_str)
            
            return JudgeScore(
                query=query,
                generated_answer=generated_answer,
                reference_answer=reference_answer,
                context=context,
                answer_relevance_score=float(scores_dict.get("answer_relevance_score", 5.0)),
                answer_faithfulness_score=float(scores_dict.get("answer_faithfulness_score", 5.0)),
                answer_completeness_score=float(scores_dict.get("answer_completeness_score", 5.0)),
                context_utilization_score=float(scores_dict.get("context_utilization_score", 5.0)),
                overall_score=float(scores_dict.get("overall_score", 5.0)),
                reasoning=scores_dict.get("reasoning", ""),
                strengths=scores_dict.get("strengths", []),
                weaknesses=scores_dict.get("weaknesses", []),
            )
            
        except Exception as e:
            # Return a default score if parsing fails
            print(f"  Warning: Failed to parse judge response: {e}")
            return JudgeScore(
                query=query,
                generated_answer=generated_answer,
                reference_answer=reference_answer,
                context=context,
                answer_relevance_score=5.0,
                answer_faithfulness_score=5.0,
                answer_completeness_score=5.0,
                context_utilization_score=5.0,
                overall_score=5.0,
                reasoning=f"Parsing error: {str(e)}",
                strengths=[],
                weaknesses=["Failed to parse judgment"],
            )
    
    @staticmethod
    def _extract_json(text: str) -> str:
        """
        Extract JSON object from text.
        
        Args:
            text: Text containing JSON
            
        Returns:
            JSON string
        """
        # Find first { and last }
        start = text.find("{")
        end = text.rfind("}") + 1
        
        if start != -1 and end > start:
            return text[start:end]
        
        raise ValueError("No JSON object found in response")
